CREATE TABLE IF NOT EXISTS raw_weather (
    _airbyte_ab_id String,
    latitude Float64,
    longitude Float64,
    resolvedAddress String,
    cloudcover Float64,
    conditions String,
    datetime String,
    datetimeEpoch Float64,
    description String,
    dew Float64,
    feelslike Float64,
    feelslikemax Float64,
    feelslikemin Float64,
    humidity Float64,
    icon String,
    moonphase Float64,
    precip Float64,
    precipcover Float64,
    precipprob Float64,
    preciptype String,
    pressure Float64,
    severerisk Float64,
    snow Float64,
    snowdepth Float64,
    solarenergy Float64,
    solarradiation Float64,
    source String,
    stations String,
    sunrise String,
    sunriseEpoch Float64,
    sunset String,
    sunsetEpoch Float64,
    temp Float64,
    tempmax Float64,
    tempmin Float64,
    uvindex Float64,
    visibility Float64,
    winddir Float64,
    windgust Float64,
    windspeed Float64,
    department String
) ENGINE = MergeTree()
ORDER BY datetime;
